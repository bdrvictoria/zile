char		*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (str[i + j] == to_find[j])
		{
			j++;
			if (str[i + j] == '\0')
				return (str + i);
		}
		i++;
	}
	return (0);
}

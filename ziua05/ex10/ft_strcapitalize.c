int			fct(char c)
{
	if (c >= '0' && c >= '0')
		return (1);
	if (c >= 'A' && c <= 'Z')
		return (1);
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}

char		*ft_strcapitalize(char *str)
{
	char *c;

	c = str;
	while (*c != '\0')
	{
		if (fct(*(c - 1)) && *c >= 'A' && *c <= 'Z')
			*c += 32;
		if (!fct(*(c - 1)) && *c >= 'a' && *c <= 'z')
			*c -= 32;
		c++;
	}
	return (str);
}
